// 🆕 Create New Customer (with auto-generated customerId)
export const createCustomer = async (req, res) => {
  try {
    const { 
      salutation,
      firstName,
      lastName,
      contactNumber, // This is phone
      whatsappNumber,
      email,
      addressLine1,
      addressLine2,
      city,
      state,
      pincode,
      notes
    } = req.body;

    // Check if phone already exists
    const existing = await Customer.findOne({ phone: contactNumber });
    if (existing) {
      return res.status(400).json({ message: "Customer with this phone already exists" });
    }

    // Create new customer with all fields (customerId will be auto-generated by pre-save hook)
    const newCustomer = await Customer.create({
      salutation: salutation || "Mr.",
      firstName,
      lastName: lastName || "",
      phone: contactNumber,
      whatsappNumber: whatsappNumber || contactNumber,
      email: email || "",
      addressLine1,
      addressLine2: addressLine2 || "",
      city: city || "",
      state: state || "",
      pincode: pincode || "",
      notes: notes || ""
    });

    console.log("✅ Customer created successfully:");
    console.log("   - ID:", newCustomer._id);
    console.log("   - Customer ID:", newCustomer.customerId);
    console.log("   - Name:", newCustomer.name);
    console.log("   - Phone:", newCustomer.phone);

    res.status(201).json({
      message: "Customer created successfully",
      customer: newCustomer
    });
  } catch (error) {
    console.error("❌ Create customer error:", error);
    res.status(500).json({ message: error.message });
  }
};

